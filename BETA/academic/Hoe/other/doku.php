<h1><a name="james_c._hoe_s_home_page" id="james_c._hoe_s_home_page">James C. Hoe&#039;s Home Page</a></h1>
<div class="level1">

<p>

I am Professor and Associate Department Head of <a href="http://www.ece.cmu.edu" class="urlextern" title="http://www.ece.cmu.edu">Electrical and Computer Engineering</a> at <a href="http://www.cmu.edu" class="urlextern" title="http://www.cmu.edu">Carnegie Mellon University</a>.  (<a href="http://www.ece.cmu.edu/directory/department/faculty/H/James_Hoe_70.html" class="urlextern" title="http://www.ece.cmu.edu/directory/department/faculty/H/James_Hoe_70.html">My official webpage</a>.) My <a href="/~jhoe/doku/doku.php?id=research" class="wikilink1" title="research">research interests</a> include computer architecture, reconfigurable computing and high-level hardware description and synthesis. I am affiliated with <a href="http://www.ece.cmu.edu/CALCM" class="urlextern" title="http://www.ece.cmu.edu/CALCM">CALCM</a> and <a href="http://www.ece.cmu.edu/~cssi" class="urlextern" title="http://www.ece.cmu.edu/~cssi">CSSI</a>. I received my Ph.D. in EECS from <a href="http://www-eecs.mit.edu/" class="urlextern" title="http://www-eecs.mit.edu/">MIT</a> in 2000 (S.M., 1994). I received my B.S. in EECS from <a href="http://www.eecs.berkeley.edu" class="urlextern" title="http://www.eecs.berkeley.edu">UC Berkeley</a> in 1992.  I am an IEEE Fellow.
</p>

