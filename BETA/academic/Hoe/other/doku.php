<h5><a name="office_hours" id="office_hours">Office Hours</a></h5>
<div class="level5">

<p>
I have office hours open to all students on all matters between 1:30 and 3:00pm on Wednesdays during the Spring and Fall semesters.  You may contact <a href="mailto:&#x6c;&#x6b;&#x69;&#x72;&#x62;&#x79;&#x40;&#x65;&#x63;&#x65;&#x2e;&#x63;&#x6d;&#x75;&#x2e;&#x65;&#x64;&#x75;" class="mail JSnocheck" title="&#x6c;&#x6b;&#x69;&#x72;&#x62;&#x79;&#x40;&#x65;&#x63;&#x65;&#x2e;&#x63;&#x6d;&#x75;&#x2e;&#x65;&#x64;&#x75;">Reenie Kirby</a> to reserve a 15-minute slot or just walk-in on a first-come-first-served basis.  It is a good idea to email me ahead of time with what you like to discuss.  You might also be able to find the answers in my 
<a href="/~jhoe/doku/doku.php?id=ms_advice_column" class="wikilink1" title="ms_advice_column">MS</a> and <a href="/~jhoe/doku/doku.php?id=advice_column" class="wikilink1" title="advice_column"> undergraduate</a> advising Blogs. During the summer or if you cannot make my office hours, please email me for appointments.
